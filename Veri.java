public class Veri {
    String sehirAdi;
    String mekanAdi;
    String onBilgi;
    String detayliBilgi;

    public Veri() {
    }

    public String getSehirAdi() {
        return sehirAdi;
    }

    public void setSehirAdi(String sehirAdi) {
        this.sehirAdi = sehirAdi;
    }

    public String getMekanAdi() {
        return mekanAdi;
    }

    public void setMekanAdi(String mekanAdi) {
        this.mekanAdi = mekanAdi;
    }

    public String getOnBilgi() {
        return onBilgi;
    }

    public void setOnBilgi(String onBilgi) {
        this.onBilgi = onBilgi;
    }

    public String getDetayliBilgi() {
        return detayliBilgi;
    }

    public void setDetayliBilgi(String detayliBilgi) {
        this.detayliBilgi = detayliBilgi;
    }
}
